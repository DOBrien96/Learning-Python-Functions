temperature = 30

if temperature > 30:
    print("It's warm")
elif temperature > 20: #elif means else if
    print("It's nice")
else:
    print("It's cold")
print("Done")

#Conditional statements are what they say, conditional, this means
#in order for the functions to fire they will need to meet certain 
#requirements, the example above is a conditional statement over
# the value of temperature.